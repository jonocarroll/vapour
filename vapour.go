package main

import (
	"github.com/devOpifex/vapour/lexer"
)

type vapour struct {
	name    string
	version string
	root    *string
	files   lexer.Files
}

func New() *vapour {
	return &vapour{
		name:    "vapour",
		version: "0.0.1",
	}
}

func addHeader(code string) string {
	return "# GENERATED BY VAPOUR\n# DO NOT EDIT\n" + code
}
